import { readFile, writeFile } from "node:fs/promises";
import { fileURLToPath } from "node:url";
import path from "node:path";

const projectRoot = path.resolve(
  path.dirname(fileURLToPath(import.meta.url)),
  "..",
);
const manuscriptPath = path.join(projectRoot, "Chhau_eBook_Content.md");
const outputPath = path.join(projectRoot, "src/content/book-pages.ts");

const markerPattern = /<!-- BOOK_PAGE (\{[^\n]+\}) -->/g;

function parsePages(source) {
  const matches = [...source.matchAll(markerPattern)];
  if (matches.length === 0) {
    throw new Error(
      "No BOOK_PAGE markers found. Expected one-line JSON metadata comments.",
    );
  }

  const pages = matches.map((match, index) => {
    const metadata = JSON.parse(match[1]);
    const bodyStart = match.index + match[0].length;
    const bodyEnd = matches[index + 1]?.index ?? source.length;
    const body = source.slice(bodyStart, bodyEnd).trim();

    if (!metadata.id || !metadata.title || !metadata.pageType) {
      throw new Error(
        `Page marker ${index + 1} must include id, title, and pageType.`,
      );
    }
    if (!["cover", "section", "content"].includes(metadata.pageType)) {
      throw new Error(`Unsupported pageType on ${metadata.id}.`);
    }

    return { ...metadata, body };
  });

  const duplicateIds = pages
    .map((page) => page.id)
    .filter((id, index, ids) => ids.indexOf(id) !== index);
  if (duplicateIds.length > 0) {
    throw new Error(`Duplicate page ids: ${[...new Set(duplicateIds)].join(", ")}`);
  }
  if (!pages.some((page) => page.id === "library")) {
    throw new Error('A page with id "library" is required for citation navigation.');
  }

  return pages;
}

function serialize(value) {
  return JSON.stringify(value, null, 2)
    .split("\n")
    .map((line, index) => (index === 0 ? line : `    ${line}`))
    .join("\n");
}

function optionalLine(key, value) {
  if (value === undefined || value === null) return "";
  return `    ${key}: ${serialize(value)},\n`;
}

function buildPage(page, pageNumber) {
  const modelUrl = page.modelUrl ?? null;
  return `  {
    id: ${JSON.stringify(page.id)},
    pageNumber: ${pageNumber},
    pageType: ${JSON.stringify(page.pageType)},
    title: ${JSON.stringify(page.title)},
    body: ${JSON.stringify(page.body)},
    modelUrl: ${JSON.stringify(modelUrl)},
    modelScale: ${page.modelScale ?? 1},
${optionalLine("modelOptions", page.modelOptions)}${optionalLine("plannedModels", page.plannedModels)}${optionalLine("coverImageUrl", page.coverImageUrl)}${optionalLine("coverImageLargeUrl", page.coverImageLargeUrl)}${optionalLine("coverImageAlt", page.coverImageAlt)}    audioUrl: null,
    audioLoop: false,
${optionalLine("interactive", page.interactive)}${optionalLine("audioTracks", page.audioTracks)}${optionalLine("gallery", page.gallery)}${optionalLine("videoUrl", page.videoUrl)}${optionalLine("videoCaption", page.videoCaption)}${optionalLine("embedUrl", page.embedUrl)}${optionalLine("embedTitle", page.embedTitle)}${optionalLine("embedCaption", page.embedCaption)}${optionalLine("embedHeight", page.embedHeight)}    showFallbackScene: false,
    ...defaultPageAppearance,
  }`;
}

function generateTypeScript(pages) {
  const renderedPages = pages
    .map((page, index) => buildPage(page, index))
    .join(",\n");

  return `// Generated by scripts/build-book-pages.mjs from Chhau_eBook_Content.md.
// Edit the manuscript, then run: npm run build:book

export type PageAnimationType =
  | "page-turn-soft"
  | "page-turn-minimal"
  | "paper-slide"
  | "sheet-glide"
  | "chapter-fade"
  | "chapter-slide"
  | "spread-open"
  | "spread-close"
  | "paper-lift"
  | "paper-settle";

export type BookPageTextStyle = {
  headingColor: string;
  bodyTextColor: string;
  fontStyle: "normal" | "italic";
  lineHeight: string;
};

export type BookPageType = "cover" | "section" | "content";

export type BookPageModelOption = {
  label: string;
  modelUrl: string;
  modelScale?: number;
};

export type BookPageAudioTrack = {
  src: string;
  title: string;
  caption?: string;
  loop?: boolean;
};

export type BookPageGalleryImage = {
  src: string;
  alt: string;
  caption?: string;
};

export type BookPageInteractive = "sandbox-guide";

export type BookPage = {
  id: string;
  pageNumber: number;
  pageType?: BookPageType;
  title: string;
  body: string;
  modelUrl: string | null;
  modelScale: number;
  modelOptions?: BookPageModelOption[];
  /** Approved asset filenames planned for this page, not loaded until modelUrl is set. */
  plannedModels?: string[];
  coverImageUrl?: string | null;
  coverImageLargeUrl?: string | null;
  coverImageAlt?: string;
  audioUrl: string | null;
  audioLoop: boolean;
  interactive?: BookPageInteractive;
  audioTracks?: BookPageAudioTrack[];
  gallery?: BookPageGalleryImage[];
  videoUrl?: string | null;
  videoCaption?: string;
  embedUrl?: string | null;
  embedTitle?: string;
  embedCaption?: string;
  embedHeight?: string;
  backgroundStyle: string;
  textStyle: BookPageTextStyle;
  animationStyle: PageAnimationType;
  modelFrameStyle: string;
  modelViewerHeight: string;
  modelViewerBackground: string;
  showFallbackScene: boolean;
};

const defaultTextStyle: BookPageTextStyle = {
  headingColor: "#2a1609",
  bodyTextColor: "#2a1609",
  fontStyle: "normal",
  lineHeight: "1.85",
};

const defaultPageAppearance = {
  backgroundStyle:
    "linear-gradient(135deg, #fff8db 0%, #fff0c4 52%, #fed7aa 100%)",
  textStyle: defaultTextStyle,
  animationStyle: "page-turn-soft" as const,
  modelFrameStyle: "border-transparent bg-transparent shadow-none",
  modelViewerHeight: "50vh",
  modelViewerBackground: "transparent",
};

export const bookPages: BookPage[] = [
${renderedPages},
];
`;
}

const manuscript = await readFile(manuscriptPath, "utf8");
const pages = parsePages(manuscript);
const generated = generateTypeScript(pages);
await writeFile(outputPath, generated);
console.log(`Generated ${pages.length} eBook pages from ${path.basename(manuscriptPath)}.`);
